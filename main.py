def input_error(func):
    """
    Декоратор для обробки помилок введення.
    """
    def inner(*args, **kwargs):
        try:
            return func(*args, **kwargs)
        except ValueError:
            return "Give me name and phone please."
        except KeyError:
            return "Contact not found."
        except IndexError:
            return "Enter user name."
            
    return inner

def parse_input(user_input):
    cmd, *args = user_input.split()
    cmd = cmd.strip().lower()
    return cmd, args

# --- Функції-обробники з декоратором ---

@input_error
def add_contact(args, contacts):
    # Спроба розпакувати args. Якщо аргументів не 2, виникне ValueError
    name, phone = args
    contacts[name] = phone
    return "Contact added."

@input_error
def change_contact(args, contacts):
    name, phone = args
    # Якщо імені немає в контактах, піднімаємо KeyError вручну, 
    # щоб декоратор його обробив (бо присвоєння значення в словник не викликає помилку)
    if name not in contacts:
        raise KeyError
    contacts[name] = phone
    return "Contact updated."

@input_error
def show_phone(args, contacts):
    # Якщо args пустий, args[0] викличе IndexError.
    name = args[0]
    # Якщо ключа немає, contacts[name] викличе KeyError.
    return contacts[name]

@input_error
def show_all(contacts):
    if not contacts:
        return "No contacts saved."
    return "\n".join([f"{name}: {phone}" for name, phone in contacts.items()])

# --- Основний цикл ---

def main():
    contacts = {}
    print("Welcome to the assistant bot!")
    
    while True:
        user_input = input("Enter a command: ")
        if not user_input:
            continue
            
        # Обробка ситуації, коли введено тільки пробіли або нічого
        try:
            command, args = parse_input(user_input)
        except ValueError:
            continue

        if command in ["close", "exit"]:
            print("Good bye!")
            break
            
        elif command == "hello":
            print("How can I help you?")
            
        elif command == "add":
            print(add_contact(args, contacts))
            
        elif command == "change":
            print(change_contact(args, contacts))
            
        elif command == "phone":
            print(show_phone(args, contacts))
            
        elif command == "all":
            print(show_all(contacts))
            
        else:
            print("Invalid command.")

if __name__ == "__main__":
    main()
